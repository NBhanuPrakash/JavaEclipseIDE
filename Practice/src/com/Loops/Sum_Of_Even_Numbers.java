package com.Loops;

import java.util.Scanner;

public class Sum_Of_Even_Numbers {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int num = sc.nextInt();
		int sum=0;
		int i=0;
		do {
			continue;
			
			
		}while(num!=0); {
			if(i%2==0) {
				sum=sum+i;
				i++;
			}
			System.out.println(sum);
			
		}
		
		
	}
	

}
